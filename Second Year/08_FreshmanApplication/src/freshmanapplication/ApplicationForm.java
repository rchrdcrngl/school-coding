    public void loadPrograms(){
        DefaultComboBoxModel<String> cbModel = (DefaultComboBoxModel) cbProgram.getModel();
        cbModel.removeAllElements();
        try {
            stm = connection.prepareStatement("SELECT * FROM programs");
            result = stm.executeQuery();
            while(result.next()){
                programsTable.put(result.getString("PROGRAM_NAME"), result.getInt("PROGRAM_ID"));
                cbModel.addElement(result.getString("PROGRAM_NAME"));
            }
        } catch (SQLException e){
            e.printStackTrace();
        }
;
    }
