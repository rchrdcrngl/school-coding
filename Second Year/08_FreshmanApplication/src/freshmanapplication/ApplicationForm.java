    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        int applicationNo=0;
        int programID = programsTable.get((String)cbProgram.getSelectedItem());
        String lastname = frmLastName.getText();
        String firstname = frmFirstName.getText();
        String middlename = frmMiddleName.getText();
        int bmonth = Integer.parseInt((String)cbBirthMonth.getSelectedItem())-1;
        int bday = Integer.parseInt((String)cbBirthDay.getSelectedItem());
        int byear = Integer.parseInt((String)cbBirthYear.getSelectedItem())-1900;
        String gender = (String) cbGender.getSelectedItem();
        String phonenumber = frmPhoneNumber.getText();
        String address = frmAddress.getText();
        try {
            stm = connection.prepareStatement("INSERT INTO applications (LAST_NAME, FIRST_NAME, MIDDLE_NAME, BIRTHDAY, GENDER, PHONE_NUMBER, ADDRESS, PROGRAM_ID) VALUES (?, ?, ?, ?, ?, ?, ?, ?)");
            stm.setString(1, lastname);
            stm.setString(2, firstname);
            stm.setString(3, middlename);
            stm.setDate(4, new Date(byear, bmonth, bday));
            stm.setString(5, gender);
            stm.setString(6, phonenumber);
            stm.setString(7, address);
            stm.setInt(8, programID);
            stm.execute();
            stm = connection.prepareStatement("SELECT LAST_INSERT_ID()");
            result = stm.executeQuery();
            if(result.next()) applicationNo = result.getInt("LAST_INSERT_ID()");
            System.out.println(""+applicationNo);
            ApplicationOverview overview = new ApplicationOverview();
            overview.setApplicationNo(applicationNo);
            overview.setVisible(true);
            dispose();
        } catch (Exception e){
            System.out.println(e.getMessage());
        };
    }//GEN-LAST:event_btnSubmitActionPerformed

